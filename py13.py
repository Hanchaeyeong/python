# page 73
a = 7
b = 2
print('{}+{} = {}'.format(a, b, a+b))       # 더하기
print('{}+{} = {}'.format(a, b, a-b))       # 빼기
print('{}+{} = {}'.format(a, b, a*b))       # 곱하기
print('{}+{} = {}'.format(a, b, a**b))      # 제곱승
print('{}+{} = {}'.format(a, b, a/b))       # 나누기(소수점까지)
print('{}+{} = {}'.format(a, b, a//b))      # 나누기(소수점 뺌)
print('{}+{} = {}'.format(a, b, a%b))       # 나머지 구하기

